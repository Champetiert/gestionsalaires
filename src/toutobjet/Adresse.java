package toutobjet;

public class Adresse {


	int numéroDeRue;
	String rue;
	int codePostal;
	String ville;
	
	public Adresse(int numéroDeRue, String rue, int codePostal, String ville) {
		this.numéroDeRue = numéroDeRue;
		this.rue = rue;
		this.codePostal = codePostal;
		this.ville = ville;
	}
}
